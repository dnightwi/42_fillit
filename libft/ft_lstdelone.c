#include <stdlib.h>
#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	t_list *fr;

	fr = *alst;
	del(fr->content, fr->content_size);
	free(*alst);
	*alst = NULL;
}
