#include <string.h>
#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t i;
	size_t j;

	i = 0;
	if (*needle == '\0')
		return ((char *)haystack);
	while (haystack[i])
	{
		j = 0;
		while (needle[j] == haystack[j + i] && i < len && needle[j])
			j++;
		if (!needle[j] && i + j <= len)
			return (((char *)haystack + i));
		i++;
	}
	return (0);
}
