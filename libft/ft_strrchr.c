char	*ft_strrchr(const char *s, int c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] == c)
			j = i;
		i++;
	}
	if (s[i] == c)
		return (((char *)s + i));
	if (j == 0 && s[0] != c)
		return (0);
	return (((char *)s + j));
}
